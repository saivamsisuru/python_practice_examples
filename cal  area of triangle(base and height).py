#calculating area of triangle with base and height
#formula is 1/2*b*h
b=float(input("enter base of the triangle: "))
h=float(input("enter height of the triangle: "))
print("base of the triangle is : {}".format(b))
print("height of the triangle is : {}".format(h))
a_o_t=1/2*(b*h)
print("Area of Triangle == {}".format(a_o_t))